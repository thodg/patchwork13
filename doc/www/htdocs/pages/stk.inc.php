<H2>STK Port to Autotools</H2>
I ported the
<a href="http://ccrma.stanford.edu/software/stk/">STK (Synthesis ToolKit)</a>
to use
<a href="http://www.gnu.org/software/autoconf/">autoconf</a>,
<a href="http://www.gnu.org/software/automake/">automake</a> and
<a href="http://pkgconfig.freedesktop.org/wiki/">pkg-config</a>.
