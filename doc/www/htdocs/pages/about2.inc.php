<font size=5>Patchwork13!</font>&nbsp;
is a real-time and modular data synthesis toolkit.
<br><br>
It is aimed to be highly portable and simple to use, allowing the user
to generate any kind of data by connecting modules (that we call patches).
<br><br><br>
We are currently working on these packages :
<TABLE border=0 cellspacing=8 style="margin-left:16px;">
<TR><TD><B>pw13</B></TD>
    <TD>&nbsp; the core library</TD>
</TR>
<TR><TD><B>pw13_std</B></TD>
    <TD>&nbsp; the standard library of patches</TD>
</TR>
<TR><TD><B>pw13_cluster</B></TD>
    <TD>&nbsp; client/server to run a patchwork over multiple computers</TD>
</TR>
<TR><TD><B>pw13_sdl</B></TD>
    <TD>&nbsp; a library of patches using the
        <a href="http://www.libsdl.org/">SDL</a>
        and <a href="http://www.opengl.org/">OpenGL</a>.</TD>
</TR>
<TR><TD><B>pw13_gtk</B></TD>
    <TD>&nbsp; a <a href="http://www.gtk.org">GTK</a> user interface</TD>
</TR>

